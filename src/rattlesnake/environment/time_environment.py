# region: TimeMetadata
class TimeMetadata(EnvironmentMetadata):
    """Storage container for parameters used by the Time Environment"""

    def __init__(
        self,
        environment_name: str = "Time",
        channel_list_bools: list = [],
        sample_rate: int = None,
        output_signal: np.array = None,
        cancel_rampdown_time: float = None,
    ):
        """
        Container to hold signal processing parameters for the Time environment

        Parameters
        ----------
        sample_rate : int
            Number of samples per second that the controller runs. This value
            dictates the sample length, not the hardware itself. Should match
            hardware but not catastrophic if it doesn't
        output_signal : np.ndarray
            Signal that will be generated by the sources, 2D array (n_outputs x
            n_samples)
        cancel_rampdown_time : float
            Time used to decay the signal to zero if the environment is stopped.
            Prevents "hard stops" from damaging equipment.

        """
        super().__init__(CONTROL_TYPE, environment_name, channel_list_bools, sample_rate)
        self.output_signal = output_signal
        self.cancel_rampdown_time = cancel_rampdown_time
        self._signal_file = None  # This is only used for saving purposes

    @property
    def signal_samples(self):
        """The number of samples in the signal"""
        return self.output_signal.shape[-1]

    @property
    def output_channels(self):
        """The number of output channels in the signal"""
        return self.output_signal.shape[0]

    @property
    def signal_time(self):
        """The length of the signal in seconds"""
        return self.signal_samples / self.sample_rate

    @property
    def cancel_rampdown_samples(self):
        """The number of samples required to ramp down the signal when cancelled"""
        return int(self.cancel_rampdown_time * self.sample_rate)

    @property
    def signal_file(self):
        return self._signal_file

    def set_file(self, filepath):
        self._signal_file = filepath

    def validate(self, hardware_metadata):
        # Prevent duplicate entries
        super().validate(hardware_metadata)

        if not isinstance(self.cancel_rampdown_time, (int, float)) or self.cancel_rampdown_time <= 0:
            raise RattlesnakeError(f"{self.environment_name} cancel_rampdown_time must be a number greater than 0")

        if not isinstance(self.sample_rate, int) or self.sample_rate <= 0:
            raise RattlesnakeError(f"{self.environment_name} sample_rate must be a number greater than 0")

        if not isinstance(self.output_signal, np.ndarray):
            raise RattlesnakeError(f"{self.environment_name} output_singal must be a 2D numpy array")

        if self.output_signal.ndim != 2:
            raise RattlesnakeError(f"{self.environment_name} output_signal must be a 2D numpy array")

        environment_channels = [channel for channel, channel_bool in zip(hardware_metadata.channel_list, self.channel_list_bools) if channel_bool]
        num_output_channels = len([channel for channel in environment_channels if channel.is_output_channel()])
        num_output = self.output_signal.shape[0]
        if num_output != num_output_channels:
            raise RattlesnakeError(f"{self.environment_name}, {num_output} signals defined for {num_output_channels} channels")

        return True

    def store_to_netcdf(
        self,
        netcdf_group_handle: nc4._netCDF4.Group,  # pylint: disable=c-extension-no-member
    ):
        """
        Stores parameters to a netCDF group so they can be recovered.

        Parameters
        ----------
        netcdf_group_handle : nc4._netCDF4.Group
            Reference to the netCDF4 group in which the environment data should
            be stored.

        """
        netcdf_group_handle.cancel_rampdown_time = self.cancel_rampdown_time
        # Save the output signal
        netcdf_group_handle.createDimension("output_channels", self.output_channels)
        netcdf_group_handle.createDimension("signal_samples", self.signal_samples)
        var = netcdf_group_handle.createVariable("output_signal", "f8", ("output_channels", "signal_samples"))
        var[...] = self.output_signal

    @classmethod
    def retrieve_metadata_from_netcdf(
        cls,
        group: nc4._netCDF4.Dataset,
        environment_name: str,
        channel_list_bools: List[bool],
        hardware_metadata: HardwareMetadata,
    ):  # pylint: disable=c-extension-no-member
        """Collects environment parameters from a netCDF dataset.

        This function retrieves parameters from a netCDF dataset that was written
        by the controller during streaming.  It must populate the widgets
        in the user interface with the proper information.

        This function is the "read" counterpart to the store_to_netcdf
        function in the TimeParameters class, which will write
        parameters to the netCDF file to document the metadata.

        Note that the entire dataset is passed to this function, so the function
        should collect parameters pertaining to the environment from a Group
        in the dataset sharing the environment's name, e.g.

        ``group = netcdf_handle.groups[self.environment_name]``
        ``self.definition_widget.parameter_selector.setValue(group.parameter)``

        Parameters
        ----------
        netcdf_handle : nc4._netCDF4.Dataset :
            The netCDF dataset from which the data will be read.  It should have
            a group name with the enviroment's name.
        """
        output_signal = group.variables["output_signal"][...].data
        cancel_rampdown_time = group.cancel_rampdown_time

        return cls(environment_name, channel_list_bools, hardware_metadata.sample_rate, output_signal, cancel_rampdown_time)

    def store_to_worksheet(self, worksheet: openpyxl.worksheet.worksheet.Worksheet):
        worksheet.cell(1, 1, "Control Type")
        worksheet.cell(1, 2, "Time")
        worksheet.cell(
            1,
            4,
            "Note: Replace cells with hash marks (#) to provide the requested parameters.",
        )
        worksheet.cell(2, 1, "Signal File")
        worksheet.cell(2, 3, "# Path to the file that contains the time signal that will be output")
        worksheet.cell(3, 1, "Cancel Rampdown Time")
        worksheet.cell(
            3,
            3,
            "# Time for the environment to ramp to zero if the environment is cancelled.",
        )

        if self.signal_file:
            worksheet.cell(2, 2, str(self.signal_file))
        if self.cancel_rampdown_time is not None:
            worksheet.cell(3, 2, str(self.cancel_rampdown_time))

    @classmethod
    def retrieve_metadata_from_worksheet(
        cls,
        worksheet: openpyxl.worksheet.worksheet.Worksheet,
        environment_name: str,
        channel_list_bools: List[bool],
        hardware_metadata: HardwareMetadata,
    ):
        for row in worksheet.rows:
            name = str(row[0].value).lower().strip().replace(" ", "_")
            value = row[1].value
            match name:
                case "control_type":
                    continue
                case "signal_file":
                    signal_file = value
                    output_signal = load_time_history(signal_file, hardware_metadata.sample_rate)
                case "cancel_rampdown_time":
                    cancel_rampdown_time = float(value)
                case "":
                    continue
                case _:
                    raise RattlesnakeError(f"{name} does not go with {CONTROL_TYPE} environment")

        metadata = cls(environment_name, channel_list_bools, hardware_metadata.sample_rate, output_signal, cancel_rampdown_time)
        if signal_file:
            metadata.set_file(signal_file)

        return metadata


